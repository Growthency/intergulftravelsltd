import 'server-only';
import { createAdminClient } from '@/lib/supabase/server';
import { packages as defaultPackages, type Pkg } from '@/lib/site';

/* ------------------------------------------------------------------ *
 *  Public package source. The website's package cards are driven by the
 *  admin-managed packages (mgmt_packages). When a type has no active
 *  package yet, we fall back to the built-in marketing cards so the site
 *  is never empty.
 * ------------------------------------------------------------------ */

type PkgType = 'hajj' | 'umrah';

type PackageRow = {
  id: string;
  type: string;
  name: string;
  price: number | null;
  year: number | null;
  description: string | null;
};

/** Format a plain number as a Taka price, e.g. 115000 → "৳1,15,000". */
function formatPrice(n: number): string {
  return '৳' + Math.round(n).toLocaleString('en-IN');
}

/** Map a DB package row onto the card shape used across the public site. */
function toPkg(row: PackageRow): Pkg {
  const highlights = (row.description ?? '')
    .split(/\r?\n/)
    .map((s) => s.trim())
    .filter(Boolean);
  return {
    id: row.id,
    type: row.type === 'umrah' ? 'umrah' : 'hajj',
    name: row.name,
    price: formatPrice(Number(row.price ?? 0)),
    priceNote: '',
    duration: row.year ? String(row.year) : '',
    highlights,
  };
}

/**
 * Active admin-managed packages for the public site, grouped by type.
 * Adding / removing a package in the admin dashboard is reflected here.
 * Falls back to the built-in marketing packages when a type has none.
 */
export async function getSitePackages(): Promise<Record<PkgType, Pkg[]>> {
  const fallback: Record<PkgType, Pkg[]> = {
    hajj: defaultPackages.filter((p) => p.type === 'hajj'),
    umrah: defaultPackages.filter((p) => p.type === 'umrah'),
  };

  try {
    const db = createAdminClient();
    const { data, error } = await db
      .from('mgmt_packages')
      .select('id, type, name, price, year, description, active')
      .eq('active', true)
      .order('price', { ascending: true });

    if (error || !data) return fallback;

    const rows = (data ?? []) as PackageRow[];
    const hajj = rows.filter((r) => r.type === 'hajj').map(toPkg);
    const umrah = rows.filter((r) => r.type === 'umrah').map(toPkg);

    return {
      hajj: hajj.length ? hajj : fallback.hajj,
      umrah: umrah.length ? umrah : fallback.umrah,
    };
  } catch {
    return fallback;
  }
}
