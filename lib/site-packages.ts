import 'server-only';
import { createAdminClient } from '@/lib/supabase/server';
import { packages as defaultPackages, type Pkg } from '@/lib/site';
import { getDictionary } from '@/lib/dictionaries';
import type { Locale } from '@/lib/i18n';

/* ------------------------------------------------------------------ *
 *  Public marketing package cards (home + /hajj/packages + /umrah/packages).
 *  Managed from admin → Website → Packages, stored as one JSON blob in
 *  site_settings (key = 'packages'). Every text field is bilingual so the
 *  Bangla and English sites each stay pure. Until an admin edits them, the
 *  built-in 3 Hajj + 3 Umrah cards are used (English from lib/site.ts,
 *  Bangla from the dictionary).
 * ------------------------------------------------------------------ */

export type L10n = { en: string; bn: string };

export type SitePackage = {
  id: string;
  name: L10n;
  price: string; // language-neutral, e.g. "৳1,15,000"
  priceNote: L10n;
  duration: L10n;
  badge: L10n;
  featured: boolean;
  highlights: L10n[]; // one entry per feature line, each with en + bn
};

export type SitePackagesDoc = { hajj: SitePackage[]; umrah: SitePackage[] };

/** The default cards, bilingual — English from lib/site.ts, Bangla from the dict. */
export function defaultSitePackages(): SitePackagesDoc {
  const enItems = getDictionary('en').home.packages.items;
  const bnItems = getDictionary('bn').home.packages.items;

  const build = (type: 'hajj' | 'umrah'): SitePackage[] =>
    defaultPackages
      .filter((p) => p.type === type)
      .map((p) => {
        const e = enItems.find((i) => i.id === p.id);
        const b = bnItems.find((i) => i.id === p.id);
        const eh = e?.highlights ?? p.highlights;
        const bh = b?.highlights ?? p.highlights;
        const n = Math.max(eh.length, bh.length);
        const highlights: L10n[] = [];
        for (let i = 0; i < n; i++) highlights.push({ en: eh[i] ?? '', bn: bh[i] ?? '' });
        return {
          id: p.id,
          name: { en: e?.name ?? p.name, bn: b?.name ?? p.name },
          price: p.price,
          priceNote: { en: e?.priceNote ?? p.priceNote, bn: b?.priceNote ?? p.priceNote },
          duration: { en: e?.duration ?? p.duration, bn: b?.duration ?? p.duration },
          badge: { en: e?.badge ?? p.badge ?? '', bn: b?.badge ?? p.badge ?? '' },
          featured: !!p.featured,
          highlights,
        };
      });

  return { hajj: build('hajj'), umrah: build('umrah') };
}

/** Coerce any stored value into an { en, bn } pair (accepts legacy strings). */
function l10n(x: unknown, fallback = ''): L10n {
  if (x && typeof x === 'object') {
    const o = x as Record<string, unknown>;
    const en = typeof o.en === 'string' ? o.en : fallback;
    const bn = typeof o.bn === 'string' ? o.bn : en;
    return { en, bn };
  }
  if (typeof x === 'string') return { en: x, bn: x };
  return { en: fallback, bn: fallback };
}

function hash(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h << 5) - h + s.charCodeAt(i);
  return h | 0;
}

function normalizePkg(x: unknown): SitePackage | null {
  if (!x || typeof x !== 'object') return null;
  const o = x as Record<string, unknown>;
  const name = l10n(o.name);
  if (!name.en && !name.bn) return null;
  return {
    id: typeof o.id === 'string' && o.id ? o.id : `pkg-${Math.abs(hash(JSON.stringify(o)))}`,
    name,
    price: typeof o.price === 'string' ? o.price : '',
    priceNote: l10n(o.priceNote),
    duration: l10n(o.duration),
    badge: l10n(o.badge),
    featured: !!o.featured,
    highlights: Array.isArray(o.highlights)
      ? o.highlights.map((h) => l10n(h)).filter((h) => h.en || h.bn)
      : [],
  };
}

/** Raw bilingual document (for the admin editor), or the default seed. */
export async function loadSitePackagesDoc(): Promise<SitePackagesDoc> {
  const fallback = defaultSitePackages();
  try {
    const db = createAdminClient();
    const { data, error } = await db
      .from('site_settings')
      .select('value')
      .eq('key', 'packages')
      .maybeSingle();
    if (error || !data?.value || typeof data.value !== 'object') return fallback;
    const v = data.value as Partial<SitePackagesDoc>;
    const keep = (arr: unknown): SitePackage[] =>
      (Array.isArray(arr) ? arr.map(normalizePkg) : []).filter((p): p is SitePackage => p !== null);
    const hajj = keep(v.hajj);
    const umrah = keep(v.umrah);
    if (!hajj.length && !umrah.length) return fallback;
    return { hajj: hajj.length ? hajj : fallback.hajj, umrah: umrah.length ? umrah : fallback.umrah };
  } catch {
    return fallback;
  }
}

/** Package cards for the public site, resolved to the given locale as `Pkg[]`. */
export async function getSitePackages(locale: Locale): Promise<{ hajj: Pkg[]; umrah: Pkg[] }> {
  const doc = await loadSitePackagesDoc();
  const pick = (l: L10n) => l[locale] || l.en || l.bn || '';
  const toPkg = (p: SitePackage, type: 'hajj' | 'umrah'): Pkg => ({
    id: p.id,
    type,
    name: pick(p.name),
    badge: pick(p.badge) || undefined,
    price: p.price,
    priceNote: pick(p.priceNote),
    duration: pick(p.duration),
    featured: p.featured,
    highlights: p.highlights.map(pick).filter(Boolean),
  });
  return {
    hajj: doc.hajj.map((p) => toPkg(p, 'hajj')),
    umrah: doc.umrah.map((p) => toPkg(p, 'umrah')),
  };
}
